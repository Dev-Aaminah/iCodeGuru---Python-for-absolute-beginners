# Bitwise operators in Python are used to perform bit-level operations on integers. These operators directly manipulate the binary representations of numbers. Python supports the following bitwise operators:

# AND (&)
# OR (|)
# NOT (~)
# XOR (^)
# Left Shift (<<)
# Right Shift (>>)

x = 12
y = 16
print(x & y)

print(x | y)

print(x ^ y)

print(~ 60)

print(3 >> 1)